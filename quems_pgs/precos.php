O Cliente Ágora dispõe de um limite de crédito equivalente a até 150% do valor da sua carteira de ações para comprar novas ações e só fazer o depósito na data da liquidação. Caso o investidor queira se financiar, ele poderá utilizar o recurso da Conta Margem após a data de liquidação da compra.
