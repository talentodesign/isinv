<p> 
<strong>TAXA DE INATIVIDADE</strong><br>
A taxa para contas sem movimentação ou posição durante 24 meses corridos (considerando sempre a última movimentação ou posição em conta) de R$ 3,00 (três reais) não será repassada para os clientes.<br>
A Ágora irá enviar um comunicado no 20º mês sem custódia ou sem operações no mercado Bovespa, informando sobre essa inatividade da conta. Se não for realizada nenhuma operação no mercado Bovespa nos 2 meses seguintes, a Ágora irá desativar a conta para a realização de operações neste mercado ao final deste período. <br>
Para reativar a conta basta entrar em contato com a Central de Atendimento Ágora.<br>
</p> 
<p> 
<strong>EMOLUMENTOS</strong><br>
Os emolumentos são cobrados pela Bovespa (Bolsa de Valores de São Paulo) e pela CBLC (Companhia Brasileira de Liquidação e Custódia) por meio de taxa fixa para cada tipo de operação ou produto. As operações normais têm incidência de 0,0345% e as Day Trade, de 0,025%.<br>
</p>
<p> 
<strong>IMPOSTO DE RENDA</strong><br>
Nas operações de Day Trade o IR é retido na fonte com alíquota de 1% sobre o ganho apurado. Nas demais operações, o IR sobre o ganho apurado deverá ser recolhido por meio do carnê-leão. Nas vendas acima de R$ 20.000,00 haverá a incidência na fonte de 0,005%.
</p>
<p>  
<strong>ISS</strong><br>
O ISS incidente sobre a taxa de corretagem tem alíquota de 2%.
</p>
